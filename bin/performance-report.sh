#!/usr/bin/env bash
set -euo pipefail

# performance-report.sh — Generate CB's weekly self-assessment
# Usage: performance-report.sh [--week YYYY-WNN]
# Pulls data from todo.db, heartbeat-state.json, error-journal.md, and knowledge files

STATE_FILE="$HOME/.openclaw/workspace/memory/heartbeat-state.json"
TODO_DB="$HOME/.openclaw/workspace/todo.db"
KNOWLEDGE_DIR="$HOME/.openclaw/workspace/memory/knowledge"
ERROR_JOURNAL="$HOME/.openclaw/workspace/memory/error-journal.md"
PERF_DIR="$HOME/.openclaw/workspace/memory/performance"
GRAPHITI_URL="http://localhost:8001"

mkdir -p "$PERF_DIR"

week="${1:-$(date +%Y-W%V)}"
report_file="$PERF_DIR/$week.md"
now=$(date +"%Y-%m-%d %H:%M %Z")

# Task stats from todo.db
if [[ -f "$TODO_DB" ]]; then
  total=$(sqlite3 "$TODO_DB" "SELECT COUNT(*) FROM entries;" 2>/dev/null || echo 0)
  done_count=$(sqlite3 "$TODO_DB" "SELECT COUNT(*) FROM entries WHERE status='done';" 2>/dev/null || echo 0)
  skipped_count=$(sqlite3 "$TODO_DB" "SELECT COUNT(*) FROM entries WHERE status='skipped';" 2>/dev/null || echo 0)
  pending_count=$(sqlite3 "$TODO_DB" "SELECT COUNT(*) FROM entries WHERE status='pending';" 2>/dev/null || echo 0)
  in_progress=$(sqlite3 "$TODO_DB" "SELECT COUNT(*) FROM entries WHERE status='in_progress';" 2>/dev/null || echo 0)
else
  total=0; done_count=0; skipped_count=0; pending_count=0; in_progress=0
fi

# Skip rate
if [[ $((done_count + skipped_count)) -gt 0 ]]; then
  skip_rate=$(awk "BEGIN {printf \"%.0f\", ($skipped_count / ($done_count + $skipped_count)) * 100}")
else
  skip_rate=0
fi

# Heartbeat counter
hb_counter=$(jq '.heartbeatCounter // 0' "$STATE_FILE" 2>/dev/null || echo 0)

# Knowledge files count
knowledge_count=$(find "$KNOWLEDGE_DIR" -name "*.md" 2>/dev/null | wc -l)

# Error count (approximate from journal)
if [[ -f "$ERROR_JOURNAL" ]]; then
  error_count=$(grep -c "^### " "$ERROR_JOURNAL" 2>/dev/null || echo 0)
else
  error_count=0
fi

# Graphiti facts count
graphiti_facts=$(curl -s -X POST "$GRAPHITI_URL/search" \
  -H "Content-Type: application/json" \
  -d '{"query": "*", "group_id": "clawdbot-main"}' 2>/dev/null | jq '.facts | length' 2>/dev/null || echo "N/A")

# Generate report
cat > "$report_file" << EOF
# CB Performance Report — $week

Generated: $now

---

## Task Metrics

| Metric | Value |
|--------|-------|
| Total tasks | $total |
| Completed | $done_count |
| Skipped | $skipped_count |
| Pending | $pending_count |
| In Progress | $in_progress |
| **Skip Rate** | **${skip_rate}%** |

## Operational Metrics

| Metric | Value |
|--------|-------|
| Heartbeats executed | $hb_counter |
| Errors encountered | $error_count |
| Knowledge files | $knowledge_count |
| Graphiti facts | $graphiti_facts |

## Skip Rate Assessment

EOF

if [[ $skip_rate -le 15 ]]; then
  echo "Skip rate is **excellent** (<= 15%). Tasks are well-scoped and achievable." >> "$report_file"
elif [[ $skip_rate -le 30 ]]; then
  echo "Skip rate is **acceptable** (16-30%). Review skipped tasks for patterns — are they blocked on human action or knowledge gaps?" >> "$report_file"
elif [[ $skip_rate -le 50 ]]; then
  echo "Skip rate is **concerning** (31-50%). Many tasks are being abandoned. Investigate: are tasks too ambitious? Is there a recurring blocker?" >> "$report_file"
else
  echo "Skip rate is **critical** (> 50%). More tasks are skipped than completed. Immediate action needed: review task creation quality, check for systemic blockers." >> "$report_file"
fi

cat >> "$report_file" << 'EOF'

## Action Items

Based on this report, CB should:
1. Review the top 3 most-skipped task categories
2. Check if knowledge gaps caused any skips (update KNOWLEDGE-MAP.md)
3. Check if errors are recurring (review error-journal.md for patterns)
4. Assess if task creation rules need tightening
5. Update MEMORY.md with key lessons from this period

---

*This report is auto-generated by `bin/performance-report.sh`. CB should review and add qualitative notes below.*

## CB's Notes

_(Add your self-assessment here)_
EOF

echo "Report written to $report_file"
echo ""
echo "=== Quick Summary ==="
echo "Tasks: $done_count done, $skipped_count skipped, $pending_count pending"
echo "Skip rate: ${skip_rate}%"
echo "Heartbeats: $hb_counter"
echo "Errors: $error_count"
echo "Knowledge files: $knowledge_count"
echo "Graphiti facts: $graphiti_facts"
